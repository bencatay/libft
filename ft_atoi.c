#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	a;
	int	res;

	i = 0;
	a = 0;
	res = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
			i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			res *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = (str[i] - '0') + (a * 10);
		i++;
	}
	return (res * a);
}
